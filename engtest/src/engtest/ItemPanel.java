    /*
     * Enimerwsi tis DB gia tin nea timi (+1) kai ton neo prosorino nikiti
     */
    private void bidBtn(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bidBtn
        if (MainFrame.username==null) {
            JOptionPane.showMessageDialog(null, "Can't buy yet!", "Login Please", JOptionPane.INFORMATION_MESSAGE);
        }else {
            if (MainFrame.username.equals(user.getText())){
                JOptionPane.showMessageDialog(null, "Already Highest Bidder !!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            }else {
                try {
                    newPrice += 1;
                    String sql = "update Items set User=? ,Price=? where ID=?";
                    Connection conn = DbConn.connect();
                    preparedStatement = conn.prepareStatement(sql);
                    preparedStatement.setString(1, MainFrame.username);
                    preparedStatement.setInt(2, newPrice);
                    preparedStatement.setInt(3, id);
                    preparedStatement.execute();
                    DbConn.disconnect(conn);
                    user.setText(MainFrame.username);
                    price.setText(Integer.toString(newPrice));
                } catch (SQLException ex) {
                    Logger.getLogger(ItemPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_bidBtn

